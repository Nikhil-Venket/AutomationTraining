package USPOLO;

public class LoadingConcepts {
    static int add(int a, int b)
    {
        return a + b;
    }

    static int add(int a, int b, int c)
    {
        return a + b + c;
    }


    public static void main(String[] args) {
        add(12,45);
        add(10,20,30);

    }
}